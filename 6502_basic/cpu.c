static void cmp(void) {
    value = getvalue();
    result = (uint16_t)a - value;

    if (a >= (uint8_t)(value & 0x00FF)) setcarry();
    else clearcarry();
    if (a == (uint8_t)(value & 0x00FF)) setzero();
    else clearzero();
    signcalc(result);
}

